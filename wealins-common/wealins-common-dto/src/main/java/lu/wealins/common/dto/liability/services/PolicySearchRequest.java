package lu.wealins.common.dto.liability.services;

public class PolicySearchRequest {

	private String polId;

	private String applicationForm;

	private String brokerRefContract;

	private String brokerId;

	private String fundId;

	private Boolean withActive;

	private Boolean withPending;

	private Boolean withInactive;

	private Integer pageNum;

	private Integer pageSize;

	public String getPolId() {
		return polId;
	}

	public void setPolId(String polId) {
		this.polId = polId;
	}

	public String getApplicationForm() {
		return applicationForm;
	}

	public void setApplicationForm(String applicationForm) {
		this.applicationForm = applicationForm;
	}

	public String getBrokerRefContract() {
		return brokerRefContract;
	}

	public void setBrokerRefContract(String brokerRefContract) {
		this.brokerRefContract = brokerRefContract;
	}

	public String getBrokerId() {
		return brokerId;
	}

	public void setBrokerId(String brokerId) {
		this.brokerId = brokerId;
	}

	public String getFundId() {
		return fundId;
	}

	public void setFundId(String fundId) {
		this.fundId = fundId;
	}

	public Boolean getWithActive() {
		return withActive;
	}

	public void setWithActive(Boolean withActive) {
		this.withActive = withActive;
	}

	public Boolean getWithPending() {
		return withPending;
	}

	public void setWithPending(Boolean withPending) {
		this.withPending = withPending;
	}

	public Boolean getWithInactive() {
		return withInactive;
	}

	public void setWithInactive(Boolean withInactive) {
		this.withInactive = withInactive;
	}

	public Integer getPageNum() {
		return pageNum;
	}

	public void setPageNum(Integer pageNum) {
		this.pageNum = pageNum;
	}

	public Integer getPageSize() {
		return pageSize;
	}

	public void setPageSize(Integer pageSize) {
		this.pageSize = pageSize;
	}
}
