package lu.wealins.common.dto.liability.services;

import java.util.Date;

public class WorkflowActionSummaryDTO {

	private String action;
	
	private Date date;
	
	private String user;
	
	private String login;

	private Integer workflowItemStatus;

	public String getAction() {
		return action;
	}

	public void setAction(String action) {
		this.action = action;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public Integer getWorkflowItemStatus() {
		return workflowItemStatus;
	}

	public void setWorkflowItemStatus(Integer workflowItemStatus) {
		this.workflowItemStatus = workflowItemStatus;
	}

	public String getLogin() {
		return login;
	}

	public void setLogin(String login) {
		this.login = login;
	}
	
}
