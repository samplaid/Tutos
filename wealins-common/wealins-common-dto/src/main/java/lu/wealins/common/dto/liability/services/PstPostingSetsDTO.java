package lu.wealins.common.dto.liability.services;

public class PstPostingSetsDTO {
	private long pstId;
	private short sapStatus;
	private short comStatus;
	private short reportStatus;
	private String sapExportDate;
	/**
	 * @return the pstId
	 */
	public long getPstId() {
		return pstId;
	}
	/**
	 * @param pstId the pstId to set
	 */
	public void setPstId(long pstId) {
		this.pstId = pstId;
	}
	/**
	 * @return the sapStatus
	 */
	public short getSapStatus() {
		return sapStatus;
	}
	/**
	 * @param sapStatus the sapStatus to set
	 */
	public void setSapStatus(short sapStatus) {
		this.sapStatus = sapStatus;
	}
	/**
	 * @return the comStatus
	 */
	public short getComStatus() {
		return comStatus;
	}
	/**
	 * @param comStatus the comStatus to set
	 */
	public void setComStatus(short comStatus) {
		this.comStatus = comStatus;
	}
	/**
	 * @return the reportStatus
	 */
	public short getReportStatus() {
		return reportStatus;
	}
	/**
	 * @param reportStatus the reportStatus to set
	 */
	public void setReportStatus(short reportStatus) {
		this.reportStatus = reportStatus;
	}
	/**
	 * @return the sapExportDate
	 */
	public String getSapExportDate() {
		return sapExportDate;
	}
	/**
	 * @param sapExportDate the sapExportDate to set
	 */
	public void setSapExportDate(String sapExportDate) {
		this.sapExportDate = sapExportDate;
	}
	
	
}
