package lu.wealins.common.dto.liability.services;

import java.math.BigDecimal;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

@JsonIgnoreProperties(ignoreUnknown = true)
public class PolicyBeneficiaryClauseDTO {

	private Long pbcId;
	private String code;
	private String fkPoliciespolId;
	private String policy;
	private Integer rank;
	private Integer status;
	private String textOfClause; // F (Free), S (Standard)
	private String type; // Death or Life
	private String typeOfClause;
	private BigDecimal percentageSplit;
	public Long getPbcId() {
		return pbcId;
	}
	public void setPbcId(Long pbcId) {
		this.pbcId = pbcId;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getFkPoliciespolId() {
		return fkPoliciespolId;
	}
	public void setFkPoliciespolId(String fkPoliciespolId) {
		this.fkPoliciespolId = fkPoliciespolId;
	}
	public String getPolicy() {
		return policy;
	}
	public void setPolicy(String policy) {
		this.policy = policy;
	}
	public Integer getRank() {
		return rank;
	}
	public void setRank(Integer rank) {
		this.rank = rank;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getTextOfClause() {
		return textOfClause;
	}
	public void setTextOfClause(String textOfClause) {
		this.textOfClause = textOfClause;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTypeOfClause() {
		return typeOfClause;
	}
	public void setTypeOfClause(String typeOfClause) {
		this.typeOfClause = typeOfClause;
	}
	public BigDecimal getPercentageSplit() {
		return percentageSplit;
	}
	public void setPercentageSplit(BigDecimal percentageSplit) {
		this.percentageSplit = percentageSplit;
	}


}
