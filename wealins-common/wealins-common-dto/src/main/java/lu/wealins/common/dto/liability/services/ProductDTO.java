package lu.wealins.common.dto.liability.services;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

@JsonIgnoreProperties(ignoreUnknown = true)
public class ProductDTO {

	private String prdId;
	private int status;
	private String name;
	private String groupCode;
	private String nlProduct;
	private String nlCountry;
	public String getPrdId() {
		return prdId;
	}
	public void setPrdId(String prdId) {
		this.prdId = prdId;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGroupCode() {
		return groupCode;
	}
	public void setGroupCode(String groupCode) {
		this.groupCode = groupCode;
	}
	public String getNlProduct() {
		return nlProduct;
	}
	public void setNlProduct(String nlProduct) {
		this.nlProduct = nlProduct;
	}
	public String getNlCountry() {
		return nlCountry;
	}
	public void setNlCountry(String nlCountry) {
		this.nlCountry = nlCountry;
	}


}
