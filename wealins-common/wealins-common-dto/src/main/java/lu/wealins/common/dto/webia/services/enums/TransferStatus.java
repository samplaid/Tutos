package lu.wealins.common.dto.webia.services.enums;

public enum TransferStatus {
	NEW("NEW"), READY("READY"), COMPTA("COMPTA"), ACCEPTED("ACCEPTED"), REFUSED("REFUSED"), EXECUTED("EXECUTED"), SPLITTED("SPLITTED");

	private TransferStatus(String code) {
		this.code = code;
	}

	private final String code;

	public String getCode() {
		return code;
	}

}
