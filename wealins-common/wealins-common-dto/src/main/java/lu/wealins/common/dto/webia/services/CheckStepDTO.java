package lu.wealins.common.dto.webia.services;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

@JsonIgnoreProperties(ignoreUnknown = true)
public class CheckStepDTO {

	private CheckWorkflowDTO check;

	private Boolean isUpdatable;

	private Boolean isMandatory;

	private Integer checkOrder;

	private String metadata;

	private String visibleIf;

	private LabelDTO label;
	
	private StepLightDTO step;

	public Boolean getIsUpdatable() {
		return isUpdatable;
	}

	public void setIsUpdatable(Boolean isUpdatable) {
		this.isUpdatable = isUpdatable;
	}

	public Boolean getIsMandatory() {
		return isMandatory;
	}

	public void setIsMandatory(Boolean isMandatory) {
		this.isMandatory = isMandatory;
	}

	public Integer getCheckOrder() {
		return checkOrder;
	}

	public void setCheckOrder(Integer checkOrder) {
		this.checkOrder = checkOrder;
	}

	public String getMetadata() {
		return metadata;
	}

	public void setMetadata(String metadata) {
		this.metadata = metadata;
	}

	public String getVisibleIf() {
		return visibleIf;
	}

	public void setVisibleIf(String visibleIf) {
		this.visibleIf = visibleIf;
	}

	public LabelDTO getLabel() {
		return label;
	}

	public void setLabel(LabelDTO label) {
		this.label = label;
	}

	public CheckWorkflowDTO getCheck() {
		return check;
	}

	public void setCheck(CheckWorkflowDTO check) {
		this.check = check;
	}

	public StepLightDTO getStep() {
		return step;
	}

	public void setStep(StepLightDTO step) {
		this.step = step;
	}

}
