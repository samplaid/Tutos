package lu.wealins.common.dto.webia.services;

public class TransferCandidate {

	private String fundId;

	private String fundName;

	private String bic;

	private String iban;

	private String currency;

	private String fundSubType;
	
	private String libDonOrd;

	public String getFundId() {
		return fundId;
	}

	public void setFundId(String fundId) {
		this.fundId = fundId;
	}

	public String getFundName() {
		return fundName;
	}

	public void setFundName(String fundName) {
		this.fundName = fundName;
	}

	public String getBic() {
		return bic;
	}

	public void setBic(String bic) {
		this.bic = bic;
	}

	public String getIban() {
		return iban;
	}

	public void setIban(String iban) {
		this.iban = iban;
	}

	public String getCurrency() {
		return currency;
	}

	public void setCurrency(String currency) {
		this.currency = currency;
	}

	public String getFundSubType() {
		return fundSubType;
	}

	public void setFundSubType(String fundSubType) {
		this.fundSubType = fundSubType;
	}

	public String getLibDonOrd() {
		return libDonOrd;
	}

	public void setLibDonOrd(String libDonOrd) {
		this.libDonOrd = libDonOrd;
	}
}
