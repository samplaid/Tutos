package lu.wealins.common.dto.liability.services;

import java.math.BigDecimal;
import java.util.Date;

public class FundTransactionPolicyDTO {

	private String policy;
	private String transactionTaxType;
	private Date effectiveDate;
	private Integer transactionId;
	private BigDecimal fundTransactionAmount;

	public String getPolicy() {
		return policy;
	}

	public void setPolicy(String policy) {
		this.policy = policy;
	}

	public String getTransactionTaxType() {
		return transactionTaxType;
	}

	public void setTransactionTaxType(String transactionTaxType) {
		this.transactionTaxType = transactionTaxType;
	}

	public Date getEffectiveDate() {
		return effectiveDate;
	}

	public void setEffectiveDate(Date effectiveDate) {
		this.effectiveDate = effectiveDate;
	}

	public Integer getTransactionId() {
		return transactionId;
	}

	public void setTransactionId(Integer transactionId) {
		this.transactionId = transactionId;
	}

	public BigDecimal getFundTransactionAmount() {
		return fundTransactionAmount;
	}

	public void setFundTransactionAmount(BigDecimal fundTransactionAmount) {
		this.fundTransactionAmount = fundTransactionAmount;
	}

}
