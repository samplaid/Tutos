package lu.wealins.common.nas;

/**
 * Thrown to indicate that NasURL is malformed.
 */
public class MalformedNasURLException extends Exception {

	private static final long serialVersionUID = -7692015625724882534L;

	/**
	 * Constructs a <code>MalformedNasURLException</code> with the specified detail message.
	 * 
	 * @param msg the detail message.
	 */
	public MalformedNasURLException(String msg) {
		super(msg);
	}

	/**
	 * Constructs a <code>MalformedNasURLException</code> with the specified detail message and the specified cause.
	 * 
	 * @param msg the detail message.
	 * @param cause the cause.
	 */
	public MalformedNasURLException(String msg, Throwable cause) {
		super(msg, cause);
	}
}
