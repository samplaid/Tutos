package lu.wealins.common.keycloak;

public class AuthentificationException extends Exception {

	/**
	 * The serial version uid
	 */
	private static final long serialVersionUID = 9056677235646340070L;

	/**
	 * Constructs a <code>AuthentificationException</code> with the specified detail message.
	 * 
	 * @param msg the detail message.
	 */
	public AuthentificationException(String msg) {
		super(msg);
	}

	/**
	 * Constructs a <code>AuthentificationException</code> with the specified cause.
	 * 
	 * @param cause the cause.
	 */
	public AuthentificationException(Throwable cause) {
		super(cause);
	}

	/**
	 * Constructs a <code>AuthentificationException</code> with the specified detail message and the specified cause.
	 * 
	 * @param msg the detail message.
	 * @param cause the cause.
	 */
	public AuthentificationException(String msg, Throwable cause) {
		super(msg, cause);
	}

}
