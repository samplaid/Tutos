package lu.wealins.webia.ws.rest.request;

public class Transco {


	private String transcoId;

	private String type;

	private String odsCode;
	
	private String scripturaCode;

	private String description;

	/**
	 * @return the transcoId
	 */
	public String getTranscoId() {
		return transcoId;
	}

	/**
	 * @param transcoId the transcoId to set
	 */
	public void setTranscoId(String transcoId) {
		this.transcoId = transcoId;
	}

	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}

	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}

	/**
	 * @return the odsCode
	 */
	public String getOdsCode() {
		return odsCode;
	}

	/**
	 * @param odsCode the odsCode to set
	 */
	public void setOdsCode(String odsCode) {
		this.odsCode = odsCode;
	}

	/**
	 * @return the scripturaCode
	 */
	public String getScripturaCode() {
		return scripturaCode;
	}

	/**
	 * @param scripturaCode the scripturaCode to set
	 */
	public void setScripturaCode(String scripturaCode) {
		this.scripturaCode = scripturaCode;
	}

	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}

	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}



	

}
