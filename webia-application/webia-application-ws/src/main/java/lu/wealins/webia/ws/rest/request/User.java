package lu.wealins.webia.ws.rest.request;

/**
 * This class represents the response of the GetGroups REST service. It is a list of a short description of the users.
 * 
 * It is designed to be fully self contained and to be marshaled to a classic JSON response payload.
 */
public class User {

	private String id;
	private String username;
	private String lastname;
	private String firstname;
	private String email;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getLastname() {
		return lastname;
	}

	public void setLastname(String lastname) {
		this.lastname = lastname;
	}

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

}
