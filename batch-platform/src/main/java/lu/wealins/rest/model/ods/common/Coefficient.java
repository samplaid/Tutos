package lu.wealins.rest.model.ods.common;

import java.math.BigDecimal;
import java.util.Date;



public class Coefficient{


	private Policy policy;

	private Fund fund;

	private Date coefDate;

	private BigDecimal coef;

	private Date creationDt;

	private Date updateDt;

	/**
	 * @return the policy
	 */
	public Policy getPolicy() {
		return policy;
	}

	/**
	 * @param policy the policy to set
	 */
	public void setPolicy(Policy policy) {
		this.policy = policy;
	}

	/**
	 * @return the fund
	 */
	public Fund getFund() {
		return fund;
	}

	/**
	 * @param fund the fund to set
	 */
	public void setFund(Fund fund) {
		this.fund = fund;
	}

	/**
	 * @return the coefDate
	 */
	public Date getCoefDate() {
		return coefDate;
	}

	/**
	 * @param coefDate the coefDate to set
	 */
	public void setCoefDate(Date coefDate) {
		this.coefDate = coefDate;
	}

	/**
	 * @return the coef
	 */
	public BigDecimal getCoef() {
		return coef;
	}

	/**
	 * @param coef the coef to set
	 */
	public void setCoef(BigDecimal coef) {
		this.coef = coef;
	}

	/**
	 * @return the creationDt
	 */
	public Date getCreationDt() {
		return creationDt;
	}

	/**
	 * @param creationDt the creationDt to set
	 */
	public void setCreationDt(Date creationDt) {
		this.creationDt = creationDt;
	}

	/**
	 * @return the updateDt
	 */
	public Date getUpdateDt() {
		return updateDt;
	}

	/**
	 * @param updateDt the updateDt to set
	 */
	public void setUpdateDt(Date updateDt) {
		this.updateDt = updateDt;
	}

	
	

}
