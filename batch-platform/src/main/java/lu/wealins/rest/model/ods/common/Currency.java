package lu.wealins.rest.model.ods.common;

import java.math.BigDecimal;
import java.util.Date;


public class Currency {



	private String fromCurrency;

	private String toCurrency;

	private Date dateRate;

	private BigDecimal middleRate;

	private Date creationDate;

	private Date updateDate;

	/**
	 * @return the fromCurrency
	 */
	public String getFromCurrency() {
		return fromCurrency;
	}

	/**
	 * @param fromCurrency the fromCurrency to set
	 */
	public void setFromCurrency(String fromCurrency) {
		this.fromCurrency = fromCurrency;
	}

	/**
	 * @return the toCurrency
	 */
	public String getToCurrency() {
		return toCurrency;
	}

	/**
	 * @param toCurrency the toCurrency to set
	 */
	public void setToCurrency(String toCurrency) {
		this.toCurrency = toCurrency;
	}

	/**
	 * @return the dateRate
	 */
	public Date getDateRate() {
		return dateRate;
	}

	/**
	 * @param dateRate the dateRate to set
	 */
	public void setDateRate(Date dateRate) {
		this.dateRate = dateRate;
	}

	/**
	 * @return the middleRate
	 */
	public BigDecimal getMiddleRate() {
		return middleRate;
	}

	/**
	 * @param middleRate the middleRate to set
	 */
	public void setMiddleRate(BigDecimal middleRate) {
		this.middleRate = middleRate;
	}

	/**
	 * @return the creationDate
	 */
	public Date getCreationDate() {
		return creationDate;
	}

	/**
	 * @param creationDate the creationDate to set
	 */
	public void setCreationDate(Date creationDate) {
		this.creationDate = creationDate;
	}

	/**
	 * @return the updateDate
	 */
	public Date getUpdateDate() {
		return updateDate;
	}

	/**
	 * @param updateDate the updateDate to set
	 */
	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}

	
	


}
