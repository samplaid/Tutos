package lu.wealins.rest.model.editing;

import java.util.List;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

@JsonIgnoreProperties(ignoreUnknown = true)
public class SearchEditingRequestRequest {

	private Long requestId;
	private List<EditingRequestStatus> status;

	public Long getRequestId() {
		return requestId;
	}

	public void setRequestId(Long requestId) {
		this.requestId = requestId;
	}

	public List<EditingRequestStatus> getStatus() {
		return status;
	}

	public void setStatus(List<EditingRequestStatus> status) {
		this.status = status;
	}

}
