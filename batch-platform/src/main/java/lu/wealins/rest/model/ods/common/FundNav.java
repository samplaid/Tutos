package lu.wealins.rest.model.ods.common;

import java.util.Collection;

/**
 * Fund Nav Model returned as component of a service response
 * 
 * @author bqv55
 *
 */
public class FundNav {

	/**
	 * Fund
	 */
	private Fund fund;

	/**
	 * NAVs of the fund
	 */
	private Collection<Nav> navs;

	public Fund getFund() {
		return fund;
	}

	public void setFund(Fund fund) {
		this.fund = fund;
	}

	public Collection<Nav> getNavs() {
		return navs;
	}

	public void setNavs(Collection<Nav> navs) {
		this.navs = navs;
	}

}
