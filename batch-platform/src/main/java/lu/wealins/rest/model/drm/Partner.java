package lu.wealins.rest.model.drm;

/**
 * The Partner information
 * 
 * @author TEY
 *
 */
public class Partner {
	/**
	 * DRM id
	 */
	private String id;
	
	/**
	 * The broker code
	 */
	private String wealins_partner_id_c;

	/**
	 * The name
	 */
	private String name;

	/**
	 * The type of partner
	 */
	private String partner_role_c;
	
	/**
	 * The lissia id
	 */
	private String lissia_id_c;
	
	private String shipping_address_city;

	/**
	 * @return the wealins_partner_id_c
	 */
	public String getWealins_partner_id_c() {
		return wealins_partner_id_c;
	}

	/**
	 * @param wealins_partner_id_c the wealins_partner_id_c to set
	 */
	public void setWealins_partner_id_c(String wealins_partner_id_c) {
		this.wealins_partner_id_c = wealins_partner_id_c;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the partner_role_c
	 */
	public String getPartner_role_c() {
		return partner_role_c;
	}

	/**
	 * @param partner_role_c the partner_role_c to set
	 */
	public void setPartner_role_c(String partner_role_c) {
		this.partner_role_c = partner_role_c;
	}

	public String getLissia_id_c() {
		return lissia_id_c;
	}

	public void setLissia_id_c(String lissia_id_c) {
		this.lissia_id_c = lissia_id_c;
	}

	public String getShipping_address_city() {
		return shipping_address_city;
	}

	public void setShipping_address_city(String shipping_address_city) {
		this.shipping_address_city = shipping_address_city;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
}
