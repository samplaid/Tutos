package lu.wealins.liability.services.ws.rest.exception;

public class WssUpdateClientException extends RuntimeException {

	private static final long serialVersionUID = -7196934991483959727L;

	private String code;

	public WssUpdateClientException(String message) {
		super(message);
	}

	public WssUpdateClientException(String message, String code) {
		super(message);
		this.code = code;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public WssUpdateClientException(Throwable cause) {
		super(cause);
	}

}
